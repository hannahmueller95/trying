mood = input("Please enter your mood: ")

if mood == 'happy':
	print("It is great to see you happy!")
elif mood == 'nervous':
	print("Take a deep breath 3 times.")
elif mood == 'sad':
	print("Today may sucked, but tomorrow is a new day!")
elif mood == 'excited':
	print("Yay!")
elif mood == 'relaxed':
	print("Wow you are so chilled out, dude.")
else:
	print("What the hell do you mean?!")
